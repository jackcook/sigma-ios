import UIKit

extension UIColor {
    
    static var darkGray: UIColor {
        return UIColor(red: 51/255, green: 51/255, blue: 51/255, alpha: 1)
    }
    
    static var fadedDarkGray: UIColor {
        return UIColor(red: 51/255, green: 51/255, blue: 51/255, alpha: 0.5)
    }
    
    static var green: UIColor {
        return UIColor(red: 52/255, green: 152/255, blue: 219/255, alpha: 1)
    }
    
    static var blue: UIColor {
        return UIColor(red: 46/255, green: 204/255, blue: 113/255, alpha: 1)
    }
}
